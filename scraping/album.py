class Album:
    def __init__(self, title, artist, release_year, rating, genre, label, reviewer, review_text, album_url):
        self.title = title
        self.artist = artist
        self.release_year = release_year
        self.rating = rating
        self.genre = genre
        self.label = label
        self.reviewer = reviewer
        self.review_text = review_text
        self.url = album_url
